    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       int id= Integer.valueOf(txtBuscar.getText());
       Tecnico tec=control.traerTecnico(id);
       txtNombre.setText(tec.getNombre());
       txtTelefono.setText(tec.getTelefono());
       txtMail.setText(tec.getMail());
       cbEspe.setSelectedIndex(recorrerCB(tec.getEspecialidad()));
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
       String nombre= txtNombre.getText();
       String mail = txtMail.getText();
       String tel = txtTelefono.getText();
       Especialidad espe=(Especialidad) cbEspe.getSelectedItem();
       control.editarTecnico(new Tecnico(Integer.valueOf(txtBuscar.getText()),nombre, espe, mail, tel));
        JOptionPane.showMessageDialog(null, "Tecnico Actualizado");
        txtNombre.setText("");
        txtMail.setText("");
        txtTelefono.setText("");
    }//GEN-LAST:event_btnActualizarActionPerformed

