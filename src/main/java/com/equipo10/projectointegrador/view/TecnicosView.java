    private void cargarCB() {
        control.traerTodasEspecialidades().stream().forEach(es->cbEspe.addItem(es));
    }
    private int recorrerCB(Especialidad espe){
        for(int i=0;i<cbEspe.getItemCount();i++){
            if(cbEspe.getItemAt(i).getId_especialidad()==(espe.getId_especialidad())){
                return i;
            }
        }
        return -1;
    }
